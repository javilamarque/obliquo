// ‚úÖ VARIABLES GLOBALES DEL ROMPECABEZAS
let pieces = []
const correctPositions = [0, 1, 2, 3, 4, 5] // Posiciones correctas
let currentPositions = [] // Posiciones actuales
let draggedElement = null
let draggedIndex = null

// ‚úÖ CONFIGURACI√ìN DEL ROMPECABEZAS
const PUZZLE_COLS = 3
const PUZZLE_ROWS = 2
const PIECE_WIDTH = 150
const PIECE_HEIGHT = 100

// ‚úÖ INICIALIZAR JUEGO
document.addEventListener("DOMContentLoaded", () => {
  console.log("üéÆ Iniciando Rompecabezas Obliquo...")
  initializePuzzle()
  setupEventListeners()
  // Mezclar despu√©s de un peque√±o delay para que se vea la imagen completa primero
  setTimeout(() => {
    shufflePieces()
  }, 1500)
})

// ‚úÖ CREAR PIEZAS DEL ROMPECABEZAS
function initializePuzzle() {
  const puzzleGrid = document.getElementById("puzzleGrid")
  puzzleGrid.innerHTML = ""
  pieces = []
  currentPositions = [0, 1, 2, 3, 4, 5]

  // Crear 6 piezas (2 filas x 3 columnas)
  for (let i = 0; i < 6; i++) {
    const piece = document.createElement("div")
    piece.className = "puzzle-piece"
    piece.draggable = true
    piece.dataset.pieceId = i
    piece.dataset.currentPosition = i

    // Calcular posici√≥n de la imagen de fondo para cada pieza
    const col = i % PUZZLE_COLS
    const row = Math.floor(i / PUZZLE_COLS)

    // Calcular el desplazamiento de la imagen de fondo
    const bgPosX = -(col * PIECE_WIDTH)
    const bgPosY = -(row * PIECE_HEIGHT)

    // Aplicar la imagen de fondo con la posici√≥n correcta
    piece.style.backgroundImage = "url('obliquo.jpg')"
    piece.style.backgroundSize = `${PUZZLE_COLS * PIECE_WIDTH}px ${PUZZLE_ROWS * PIECE_HEIGHT}px`
    piece.style.backgroundPosition = `${bgPosX}px ${bgPosY}px`
    piece.style.backgroundRepeat = "no-repeat"

    // Event listeners para drag & drop
    piece.addEventListener("dragstart", handleDragStart)
    piece.addEventListener("dragover", handleDragOver)
    piece.addEventListener("drop", handleDrop)
    piece.addEventListener("dragend", handleDragEnd)

    pieces.push(piece)
    puzzleGrid.appendChild(piece)
  }

  updateProgress()
  console.log("üß© Rompecabezas inicializado con 6 piezas")
}

// ‚úÖ CONFIGURAR EVENT LISTENERS
function setupEventListeners() {
  const shuffleBtn = document.getElementById("shuffleBtn")
  const resetBtn = document.getElementById("resetBtn")

  shuffleBtn.addEventListener("click", shufflePieces)
  resetBtn.addEventListener("click", resetPuzzle)

  console.log("üéõÔ∏è Event listeners configurados")
}

// ‚úÖ DRAG & DROP HANDLERS
function handleDragStart(e) {
  draggedElement = e.target
  draggedIndex = Number.parseInt(e.target.dataset.currentPosition)
  e.target.classList.add("dragging")

  console.log(`üñ±Ô∏è Arrastrando pieza desde posici√≥n ${draggedIndex}`)
}

function handleDragOver(e) {
  e.preventDefault()
  e.target.classList.add("drag-over")
}

function handleDrop(e) {
  e.preventDefault()
  e.target.classList.remove("drag-over")

  if (e.target.classList.contains("puzzle-piece") && e.target !== draggedElement) {
    const targetIndex = Number.parseInt(e.target.dataset.currentPosition)
    swapPieces(draggedIndex, targetIndex)
    console.log(`üîÑ Intercambiando piezas: ${draggedIndex} ‚Üî ${targetIndex}`)
  }
}

function handleDragEnd(e) {
  e.target.classList.remove("dragging")
  document.querySelectorAll(".puzzle-piece").forEach((piece) => {
    piece.classList.remove("drag-over")
  })
  draggedElement = null
  draggedIndex = null
}

// ‚úÖ INTERCAMBIAR PIEZAS
function swapPieces(index1, index2) {
  const piece1 = pieces.find((p) => Number.parseInt(p.dataset.currentPosition) === index1)
  const piece2 = pieces.find((p) => Number.parseInt(p.dataset.currentPosition) === index2)

  if (piece1 && piece2) {
    // Intercambiar posiciones en el array
    const pieceId1 = Number.parseInt(piece1.dataset.pieceId)
    const pieceId2 = Number.parseInt(piece2.dataset.pieceId)

    currentPositions[index1] = pieceId2
    currentPositions[index2] = pieceId1

    // Actualizar dataset
    piece1.dataset.currentPosition = index2
    piece2.dataset.currentPosition = index1

    // Intercambiar en el DOM
    const puzzleGrid = document.getElementById("puzzleGrid")
    const tempNextSibling = piece1.nextSibling
    puzzleGrid.insertBefore(piece1, piece2.nextSibling)
    puzzleGrid.insertBefore(piece2, tempNextSibling)

    updateProgress()
    checkCompletion()
  }
}

// ‚úÖ MEZCLAR PIEZAS
function shufflePieces() {
  console.log("üîÄ Mezclando piezas...")

  // Crear array de √≠ndices y mezclarlo
  const shuffledIndices = [0, 1, 2, 3, 4, 5]

  // Algoritmo Fisher-Yates para mezclar
  for (let i = shuffledIndices.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1))
      ;[shuffledIndices[i], shuffledIndices[j]] = [shuffledIndices[j], shuffledIndices[i]]
  }

  // Reorganizar piezas en el DOM seg√∫n el nuevo orden
  const puzzleGrid = document.getElementById("puzzleGrid")
  puzzleGrid.innerHTML = ""

  shuffledIndices.forEach((pieceId, position) => {
    const piece = pieces[pieceId]
    piece.dataset.currentPosition = position
    currentPositions[position] = pieceId
    puzzleGrid.appendChild(piece)
  })

  updateProgress()
  console.log("‚úÖ Piezas mezcladas")
}

// ‚úÖ REINICIAR ROMPECABEZAS
function resetPuzzle() {
  console.log("üîÑ Reiniciando rompecabezas...")
  currentPositions = [0, 1, 2, 3, 4, 5]

  const puzzleGrid = document.getElementById("puzzleGrid")
  puzzleGrid.innerHTML = ""

  pieces.forEach((piece, index) => {
    piece.dataset.currentPosition = index
    puzzleGrid.appendChild(piece)
  })

  updateProgress()
  checkCompletion()
  console.log("‚úÖ Rompecabezas reiniciado")
}

// ‚úÖ ACTUALIZAR PROGRESO
function updateProgress() {
  let correctPieces = 0

  currentPositions.forEach((pieceId, position) => {
    if (pieceId === position) {
      correctPieces++
    }
  })

  const progressText = document.getElementById("progressText")
  const progressFill = document.getElementById("progressFill")

  progressText.textContent = `${correctPieces} / 6`
  progressFill.style.width = `${(correctPieces / 6) * 100}%`

  console.log(`üìä Progreso: ${correctPieces}/6 piezas correctas`)
}

// ‚úÖ VERIFICAR COMPLETACI√ìN
function checkCompletion() {
  const isComplete = currentPositions.every((pieceId, position) => pieceId === position)

  if (isComplete) {
    console.log("üéâ ¬°Rompecabezas completado!")
    setTimeout(() => {
      showCelebrationModal()
    }, 500)
  }
}

// ‚úÖ MOSTRAR MODAL DE CELEBRACI√ìN
function showCelebrationModal() {
  const modal = document.getElementById("celebrationModal")
  modal.classList.remove("hidden")

  // Crear confetti
  createConfetti()

  // Esperar 3 segundos y mostrar video
  setTimeout(() => {
    modal.classList.add("hidden")
    showCompletionVideo()
  }, 3000)

  console.log("üéä Modal de celebraci√≥n mostrado")
}

// ‚úÖ MOSTRAR VIDEO DE COMPLETACI√ìN CON CONTROLES Y SONIDO
function showCompletionVideo() {
  const videoModal = document.getElementById("videoModal")
  const video = document.getElementById("completionVideo")
  const skipBtn = document.getElementById("skipVideoBtn")
  const muteBtn = document.getElementById("muteBtn")

  videoModal.classList.remove("hidden")

  // ‚úÖ CONFIGURAR VIDEO CON SONIDO
  video.currentTime = 0
  video.volume = 0.7 // Volumen al 70%
  video.muted = false // ‚úÖ HABILITAR SONIDO

  // ‚úÖ CONFIGURAR BOT√ìN DE SALTAR
  skipBtn.addEventListener("click", () => {
    console.log("‚è≠Ô∏è Usuario salt√≥ el video")
    redirectToBienvenida()
  })

  // ‚úÖ CONFIGURAR BOT√ìN DE MUTE/UNMUTE
  muteBtn.addEventListener("click", () => {
    if (video.muted) {
      video.muted = false
      muteBtn.innerHTML = '<span class="icon">üîä</span>'
      console.log("üîä Video desmutado")
    } else {
      video.muted = true
      muteBtn.innerHTML = '<span class="icon">üîá</span>'
      console.log("üîá Video muteado")
    }
  })

  // ‚úÖ REPRODUCIR VIDEO
  video.play().catch((error) => {
    console.log("Error al reproducir video:", error)
    // Si hay error, mostrar bot√≥n para continuar manualmente
    setTimeout(() => {
      redirectToBienvenida()
    }, 2000)
  })

  // ‚úÖ CUANDO TERMINE EL VIDEO, REDIRIGIR AUTOM√ÅTICAMENTE
  video.addEventListener("ended", () => {
    console.log("üé¨ Video terminado, redirigiendo...")
    setTimeout(() => {
      redirectToBienvenida()
    }, 1000)
  })

  // ‚úÖ PERMITIR DOBLE CLICK PARA SALTAR
  video.addEventListener("dblclick", () => {
    console.log("üñ±Ô∏è Doble click en video, redirigiendo...")
    redirectToBienvenida()
  })

  // ‚úÖ TECLA ESC PARA SALTAR
  const handleEscKey = (e) => {
    if (e.key === "Escape") {
      console.log("‚å®Ô∏è ESC presionado, redirigiendo...")
      redirectToBienvenida()
      document.removeEventListener("keydown", handleEscKey)
    }
  }
  document.addEventListener("keydown", handleEscKey)

  console.log("üé¨ Video de completaci√≥n iniciado con controles")
}

// ‚úÖ REDIRIGIR A BIENVENIDA
function redirectToBienvenida() {
  const videoModal = document.getElementById("videoModal")
  const video = document.getElementById("completionVideo")

  // Pausar y limpiar video
  if (video) {
    video.pause()
    video.currentTime = 0
  }

  // Efecto de transici√≥n
  if (videoModal) {
    videoModal.style.transition = "opacity 0.5s ease"
    videoModal.style.opacity = "0"
  }

  setTimeout(() => {
    console.log("üè† Redirigiendo a bienvenida.html...")
    window.location.href = "bienvenida.html"
  }, 500)
}

// ‚úÖ CREAR EFECTO CONFETTI
function createConfetti() {
  const colors = ["#a3e635", "#84cc16", "#65a30d", "#4ade80", "#22c55e", "#16a34a"]

  for (let i = 0; i < 50; i++) {
    setTimeout(() => {
      const confetti = document.createElement("div")
      confetti.style.position = "fixed"
      confetti.style.left = Math.random() * 100 + "vw"
      confetti.style.top = "-10px"
      confetti.style.width = "10px"
      confetti.style.height = "10px"
      confetti.style.backgroundColor = colors[Math.floor(Math.random() * colors.length)]
      confetti.style.borderRadius = "50%"
      confetti.style.pointerEvents = "none"
      confetti.style.zIndex = "9999"
      confetti.style.transition = "all 3s ease-out"

      document.body.appendChild(confetti)

      setTimeout(() => {
        confetti.style.top = "100vh"
        confetti.style.transform = "rotate(720deg)"
        confetti.style.opacity = "0"
      }, 100)

      setTimeout(() => {
        if (document.body.contains(confetti)) {
          document.body.removeChild(confetti)
        }
      }, 3000)
    }, i * 50)
  }

  console.log("üéä Confetti creado")
}

